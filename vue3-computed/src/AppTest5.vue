<template>
  <h1
    :class="{ active: isActive }"
    @click="activate">
    Hello?!({{ isActive }})
  </h1>
</template>

<script>
export default {
  data() {
    return {
      isActive: false
    }
  },
  methods: {
    activate() {
      this.isActive = !this.isActive
    }
  }
}
</script>

<style lang="scss" scoped>
  .active {
    color: red;
    font-weight: bold;
  }
</style>